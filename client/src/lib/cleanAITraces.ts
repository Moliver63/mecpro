// Emojis Unicode (todos os intervalos conhecidos)
const EMOJI_RE = /[\u{1F300}-\u{1F9FF}\u{2600}-\u{26FF}\u{2700}-\u{27BF}\u{FE00}-\u{FE0F}\u{1F000}-\u{1FFFF}\u{200D}\u{20E3}\u{FE0F}\u{1F1E0}-\u{1F1FF}]/gu;

// Frases de IA em comentĂˇrios ou inĂ­cio de linha
const AI_PHRASES_RE = /^\s*(Generated by|AI-assisted|AI generated|Copilot|ChatGPT|GPT-4|Claude:|Assistant:|Certainly!|Here's how|Below is|Let me explain|Sure,|Of course,|<AI>|\[AI\]|<!--\s*AI\s*-->)\s*/gim;

// TraĂ§os tipogrĂˇficos em comentĂˇrios/strings â€” substitui por hĂ­fen simples
const EM_DASH_RE = /[â€”â€“â€•]/g;

// ComentĂˇrios de linha Ăşnica com palavras genĂ©ricas de IA
// Cuidado: nĂŁo remover "--" como operador (decremento, etc)
const AI_COMMENT_SINGLE_RE = /\/\/\s*(inicia|inicializa|define|loop|funĂ§ĂŁo|calcula|retorna|exemplo|nota|observaĂ§ĂŁo|atenĂ§ĂŁo|passo|etapa|aqui|abaixo|acima|conforme|verificar?|checar?|todo|fixme|hack|review|generated|copilot|chatgpt|ai:)\b[^\n]*/gi;

// TraĂ§os longos (--) SOMENTE em comentĂˇrios de linha (// ...) ou blocos (/* */)
// Deixa intacto em cĂłdigo real (decremento, CSS, etc)
const COMMENT_DASH_RE = /(\/\/[^\n]*?)\s*â€”\s*/g;

// Linhas vazias extras (3+ seguidas â†’ 2 no mĂˇximo)
const EXTRA_BLANK_LINES_RE = /\n{3,}/g;

/**
 * Remove rastros de IA de um texto/cĂłdigo sem quebrar a lĂłgica.
 * @param text â€” texto a limpar
 * @param options â€” configuraĂ§Ăµes opcionais
 */
export function cleanAITraces(
  text: string,
  options: {
    removeEmojis?:   boolean; // default: true
    removeComments?: boolean; // default: true (somente comentĂˇrios genĂ©ricos de IA)
    removePhrases?:  boolean; // default: true
    fixDashes?:      boolean; // default: true
    fixBlankLines?:  boolean; // default: true
  } = {}
): string {
  const {
    removeEmojis   = true,
    removeComments = true,
    removePhrases  = true,
    fixDashes      = true,
    fixBlankLines  = true,
  } = options;

  let result = text;

  // 1. Remove frases de IA no inĂ­cio de linhas
  if (removePhrases) {
    result = result.replace(AI_PHRASES_RE, "");
  }

  // 2. Remove comentĂˇrios genĂ©ricos de IA (// inicializa, // define, etc.)
  if (removeComments) {
    result = result.replace(AI_COMMENT_SINGLE_RE, "");
  }

  // 3. Substitui traĂ§os tipogrĂˇficos em comentĂˇrios por hĂ­fen
  if (fixDashes) {
    result = result.replace(COMMENT_DASH_RE, (_, comment) =>
      comment.replace(EM_DASH_RE, "-")
    );
    // TraĂ§os tipogrĂˇficos soltos fora de cĂłdigo
    result = result.replace(EM_DASH_RE, "-");
  }

  // 4. Remove emojis
  if (removeEmojis) {
    result = result.replace(EMOJI_RE, "");
  }

  // 5. Reduz linhas em branco excessivas (3+ â†’ 2)
  if (fixBlankLines) {
    result = result.replace(EXTRA_BLANK_LINES_RE, "\n\n");
  }

  // 6. Limpa espaĂ§os extras no final de cada linha
  result = result.replace(/[ \t]+$/gm, "");

  return result.trim();
}

/**
 * VersĂŁo leve â€” apenas para textos de marketing/copy (nĂŁo cĂłdigo).
 * Remove emojis, frases de IA e traĂ§os tipogrĂˇficos.
 * Ideal para onChange em campos de texto da campanha.
 */
export function cleanAICopy(text: string): string {
  return cleanAITraces(text, {
    removeEmojis:   true,
    removeComments: false, // copy nĂŁo tem comentĂˇrios de cĂłdigo
    removePhrases:  true,
    fixDashes:      true,
    fixBlankLines:  true,
  });
}

/**
 * Hook para usar em onChange de textarea â€” throttled para nĂŁo travar digitaĂ§ĂŁo.
 * Retorna { value, onChange } para espalhar no textarea.
 */
export function useCleanTextarea(
  value: string,
  setValue: (v: string) => void,
  mode: "copy" | "code" = "copy"
) {
  const handleChange = (e: React.ChangeEvent<HTMLTextAreaElement>) => {
    const raw = e.target.value;
    // SĂł limpa quando o usuĂˇrio para de digitar (onBlur vai limpar mais profundamente)
    setValue(raw);
  };

  const handleBlur = () => {
    const cleaned = mode === "code"
      ? cleanAITraces(value)
      : cleanAICopy(value);
    if (cleaned !== value) setValue(cleaned);
  };

  return { value, onChange: handleChange, onBlur: handleBlur };
}

export default cleanAITraces;
